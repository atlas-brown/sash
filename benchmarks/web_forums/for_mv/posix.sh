#!/bin/sh

# https://unix.stackexchange.com/questions/231513/bash-move-files-of-specific-pattern

ARCHIVE_FILEMASK="????-??-??_E_MDT_0_0.7z"
FILEMASK="?????????_?????_A_?_????????????????-?????????"
extractDir=/path/to/dir
dest=/path/to/dest

for f in ${ARCHIVE_FILEMASK}
do
    if 7z e -aoa -o"${extractDir}" "$f";
    then
        mv "${extractDir}/${FILEMASK}".xml "$dest"
    fi
done

